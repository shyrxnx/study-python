fruits = ["apple", "banana", "orange", "watermelon", "grape", "cherries", "mango"]

# Iterates over the array and prints each fruit in every iteration
for fruit in fruits:
    print(fruit)

# Sorts the array in alphabetical order
fruits.sort()
print(fruits)

# Sorts the array in reverse alphabetical order
fruits.sort(reverse=True)
print(fruits)
